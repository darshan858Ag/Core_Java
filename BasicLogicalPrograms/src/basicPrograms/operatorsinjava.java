package basicPrograms;

public class operatorsinjava {
	public static void main(String[] args)
	{
		// arithmetic operators
		System.out.println(2+2);
		System.out.println(2-1);
		System.out.println(3*7);
		System.out.println(10/2);
		System.out.println(10%2);
		
		//relational operators
		System.out.println(11<8);
		System.out.println(20>2);
		System.out.println(22<=22);
		System.out.println(67<=67);
		System.out.println(46==46);
		System.out.println(45!=46);
		
		
	   //logical operators
		System.out.println((20>30)&&(20>40));
		System.out.println((20>50)&&(60>80));
		System.out.println((70>30)&&(10>60));
		
		System.out.println(('c'=='c')||('d'=='b'));
		boolean paper=true;
		System.out.println(paper);
		System.out.println(!paper);
		
}
}
