package IterativeStatements;

public class Range_Decending_for {
	public static void main(String[] args) {
	
		System.out.println("Vertical orientation:");
	int i;
	for(i=70;i>=50;i--)
	{
		System.out.println(i);
	}
	System.out.println();
	System.out.println("Horizontal orientation:");
	for(i=70;i>=50;i--)
	{
		System.out.print(i+" ");
	}
	
}
}