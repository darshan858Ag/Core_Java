package IterativeStatements;

public class squarecube {
	public static void main(String[] args) {
		System.out.println("square series:");
		for(int i=1;i<=10;i++)
		{
			
				System.out.print(i*i+" ");
		}
		
		System.out.println();
		System.out.println("cube series:");
		for(int i=1;i<=10;i++)
		{
		
			
				System.out.print(i*i*i+" ");
			
		}
}
}
